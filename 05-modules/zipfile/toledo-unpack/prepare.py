def generate_name():
    fname = random.choice(fnames)
    lname = random.choice(lnames)
    return f"{fname} {lname}"


def generate_qid():
    n = random.randrange(1000000, 9999999)
    return f'q{n}'


def generate_text_file(qid, name):
    return f"Naam: {name} ({qid})"


def generate_data_file():
    return str(random.randrange(99999999))


if len(sys.argv) != 2:
    print('Provide a seed (e.g. python prepare.py 123)')
    sys.exit(-1)

random.seed(int(sys.argv[1]))

with ZipFile('package.zip', 'w') as zip:
    for _ in range(random.randrange(5, 20)):
        name = generate_name()
        qid = generate_qid()
        d = random.randint(1, 28)
        m = random.randint(1, 12)
        y = random.randint(2000, 2018)
        text_filename = f'Submission_{qid}_{d}-{m}-{y}.txt'
        submission_filename = f'Submission_{qid}_{d}-{m}-{y}.js'

        with zip.open(text_filename, 'w') as f:
            f.write(generate_text_file(qid, name).encode('utf-8'))

        with zip.open(submission_filename, 'w') as f:
            f.write(generate_data_file().encode('utf-8'))

print("There you go! I created a file package.zip for you.")